#Descending Order
cars = ["bmw","audi","toyota","subaru","zebra"]
cars.sort(reverse=True)
print (cars)

#Ascending Order
cars = ["bmw","audi","toyota","subaru","zebra"]
cars.sort()
print (cars)

#Sorting a List Temporarily with the sorted() Function

cars = ["bmw","audi","toyota","subaru","zebra"]
print (sorted(cars))


#Reverse List

cars = ["bmw","audi","toyota","subaru","zebra"]

cars.reverse()

print(cars)

#Length Of The Function

print (len(cars))